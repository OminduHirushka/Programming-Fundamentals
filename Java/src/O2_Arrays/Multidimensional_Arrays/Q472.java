package O2_Arrays.Multidimensional_Arrays;

public class Q472 {
    public static void main(String[] args) {
        int[] a;
        a = new int[5];
        System.out.println(a);
        a[0] = 100;
        System.out.println(a[0]);

        int[][] b;
        b = new int[3][2];
        System.out.println(b);
        System.out.println(b[0]);
        b[0][1] = 100;
        System.out.println(b[0][1]);
    }
}
