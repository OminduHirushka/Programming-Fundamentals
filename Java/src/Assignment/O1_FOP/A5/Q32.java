package Assignment.O1_FOP.A5;

public class Q32 {
    public static void main(String[] args) {

        System.out.println("For Loop:");
        for (int i=0; i<10; i++) {
            System.out.println("    Hello World");
        }

        System.out.println("\nWhile Loop:");
        int j = 0;
        while (j<10) {
            System.out.println("    Hello World");
            j++;
        }
    }
}
